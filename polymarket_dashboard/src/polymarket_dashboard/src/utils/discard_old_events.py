import time


async def discard_old_events(current_asset_ids: dict[str, int]) -> dict[str, int]:
    """Discard old events from current asset ids.

    Args:
        current_asset_ids (dict[str, int]): Current asset ids.
    """
    now = int(time.time())
    return {
        asset_id: timestamp
        for asset_id, timestamp in current_asset_ids.items()
        if timestamp > now  # valid event
    }



